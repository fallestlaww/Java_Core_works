package ua.lviv.lgs.third;

public class Frog extends Amphibia{

	@Override
	void eat() {
		System.out.println("Frog is eating");
		
	}

	@Override
	void sleep() {
		System.out.println("Frog is sleeping");
		
	}

	@Override
	void swim() {
		System.out.println("Frog is swiming");
		
	}

	@Override
	void walk() {
		System.out.println("Frog is walking");
	}

}
